#!/bin/bash
# Set variables for stdout print

COLOR_N='\e[0m'
COLOR_GREEN='\e[1;32m'
COLOR_RED='\e[1;31m'
COLOR_BLUE='\e[1;34m'
OK="[${COLOR_GREEN}success${COLOR_N}]"
FAIL="[${COLOR_RED}fail${COLOR_N}]"
WARNING="[${COLOR_RED}warning${COLOR_N}]"
INFO="[${COLOR_BLUE}info${COLOR_N}]"
PROGRESS="[${COLOR_BLUE}..${COLOR_N}]"

